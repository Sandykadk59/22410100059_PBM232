//Tampilkan konversi nilai ke huruf

void main() {
  var n = 75;

  if (n>=90 && n<=100) {
    print('A');
  } else if (n>=80 && n<=90) {
    print('B');
  } else if (n>=70 && n<=80) {
    print('C');
  } else if (n>=60 && n<=70) {
    print('D');
  } else if (n>=50 && n<=60) {
    print('E');
  }
}